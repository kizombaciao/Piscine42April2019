char	*ft_strncat(char *dest, char *src, int nb)
{
	char *tmp;

	tmp = dest;
	while (*dest)
	{
		dest++;
	}
	while (nb--)
	{
		*dest++ = *src++;
	}
	*dest = '\0';
	return (tmp);
}
