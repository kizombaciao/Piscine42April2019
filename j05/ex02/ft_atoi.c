int		ft_atoi(char *str)
{
	int i;
	int sign;
	int nb;

	i = 0;
	while (str[i] && (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' \
				|| str[i] == ' ' || str[i] == '\f' || str[i] == '\r'))
		i++;
	sign = 1;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	nb = 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	return (sign * nb);
}
