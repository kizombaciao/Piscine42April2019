#include "temp.h"

int		rows(char *s)
{
	int rows;
	int i;

	rows = 0;
	i = 0;
	while ( s[i] != '\0')
	{
		if (s[i] == '\n')
		{
			rows++;
		}
		i++;
	}
	return (rows);
}
